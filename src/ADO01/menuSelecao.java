    private void jBInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBInicioActionPerformed
        // TODO add your handling code here:
        Carros novoCarro = new Carros();
        novoCarro.setModelo(jTFModelo.getText());
        novoCarro.setMarca(jTFMarca.getText());
        novoCarro.setAno(Integer.parseInt(jTFAno.getText()));
        No novoNo = new No(novoCarro);
        novoNo.c = novoCarro;

        try {
            listaSimples.inserirPrimeiro(novoCarro);
            //JOptionPane.showMessageDialog(null, "carro inserido com sucesso", "confirmação de inserção",
                    //JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar inserir o carro", "Erro", JOptionPane.ERROR_MESSAGE);
        }

        jTFModelo.setText("");
        jTFMarca.setText("");
        jTFAno.setText("");

    }//GEN-LAST:event_jBInicioActionPerformed

