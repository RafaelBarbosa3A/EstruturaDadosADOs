    private void jBRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoverActionPerformed
        // TODO add your handling code here:
        String modeloRemovido;
        try {
            JOptionPane.showInputDialog("digite o modelo a ser excluído");
            modeloRemovido = leitor.next();
            JOptionPane.showMessageDialog(null, "confirma a remoção do carro?", "o modelo foi removido",
                    JOptionPane.YES_NO_OPTION);
            listaSimples.removerNo(modeloRemovido);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao tentar remover o carro", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jBRemoverActionPerformed

